def parse(motif):
    rows = [r for r in motif.strip("\n").splitlines()]
    cells = []
    for y, row in enumerate(rows):
        for x, ch in enumerate(row):
            if ch == '#':
                cells.append((x, y, 'primary'))
            elif ch == '@':
                cells.append((x, y, 'amber'))
            elif ch == ',':
                cells.append((x, y, 'muted'))
    return cells


def render(brand, motif):
    img = Image.new('RGBA', (GRID * SCALE, GRID * SCALE), (0, 0, 0, 0))
    d = ImageDraw.Draw(img)
    primary = BRAND[brand]
    for x, y, role in parse(motif):
        if role == 'primary':
            color = primary + (255,)
        elif role == 'amber':
            color = AMBER + (255,)
        else:
            color = MUTED + (255,)
        d.rectangle(
            (x * SCALE, y * SCALE, (x + 1) * SCALE - 1, (y + 1) * SCALE - 1),
            fill=color,
        )
    return img


def main():
    out = Path(__file__).resolve().parent / 'icons'
    out.mkdir(exist_ok=True)
    for brand, motif in MOTIFS.items():
        img = render(brand, motif)
        img.save(out / f'{brand}.png')
    print(f"wrote {len(MOTIFS)} icons to {out}")


if __name__ == '__main__':
    main()
